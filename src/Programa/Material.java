package Programa;

/**
 *
 * @author ericr
 */
public class Material {
    private int id;
    private String nombre;
    private int anio;
    private String editorial;
    private String titulo;
    private String autor;
    private int anioPublicacion;

    // Constructor para RegistrosImpl
    public Material() {} // Constructor vacío necesario

    // Constructor para tu lógica actual
    public Material(String titulo, String autor, int anioPublicacion) {
        this.titulo = titulo;
        this.autor = autor;
        this.anioPublicacion = anioPublicacion;
    }

    // Setters y Getters para todos los campos
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public String getEditorial() {
        return editorial;
    }

    public void setEditorial(String editorial) {
        this.editorial = editorial;
    }

    // Mantén tus métodos existentes
    public void mostrarDetalles() {
        System.out.println("Título: " + titulo);
        System.out.println("Autor: " + autor);
        System.out.println("Año de publicación: " + anioPublicacion);
    }

    public void actualizarInfo(String nuevoTitulo, String nuevoAutor, int nuevoAnio) {
        this.titulo = nuevoTitulo;
        this.autor = nuevoAutor;
        this.anioPublicacion = nuevoAnio;
    }

    // Getters/Setters originales
    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public int getAnioPublicacion() {
        return anioPublicacion;
    }

    public void setAnioPublicacion(int anioPublicacion) {
        this.anioPublicacion = anioPublicacion;
    }
}
